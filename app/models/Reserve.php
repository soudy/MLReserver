class Reserve extends Model
{
    public function get_all_requests()
    {
        $sql = 'SELECT * FROM reserved_items';

        $query = $this->db->prepare($sql);
        $query->execute();

        return $query->fetchAll(PDO::FETCH_OBJ);
    }
}
